#include "ft_string.h"

size_t	ft_strlen(const char *str)
{
	size_t	i;

	i = 0;
	while (str[i])
		i++;
	return (i);
}

size_t	ft_strlen_dptr(char **ptr)
{
	size_t	i;

	i = 0;
	while (ptr[i])
		i++;
	return (i);
}
